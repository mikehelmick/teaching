public class Lab2 {
 
    public static void main( String[] args ) {
        
        // convert a value from yards to meters
        double distance = 2365.35;
        double meters = distance * 0.9144;
        System.out.println( distance + " yards is " + meters + " meters." );
        
        // convert a temp from fahrenheit to celsius
        double fahrenheit = 76.5;
        double celsius = (fahrenheit - 32) / 1.8;
        System.out.println( fahrenheit + " fahrenheit is " + celsius + " celsius." );
           
        // convert degrees to radians
        double degrees = 95;
        double radians = degrees * Math.PI / 180;
        System.out.println( degrees + " degrees is " + radians + " radians.");
        
    }
    
}